package com.company;

public class Kitchen extends Room {
    public Kitchen() {}

    int fridgeCount, cabinetsCount, stoveCount, ovenCount, sinkCount;

    public int getFridgeCount() {
        return fridgeCount;
    }

    public void setFridgeCount(int fridgeCount) {
        this.fridgeCount = fridgeCount;
    }

    public int getCabinetsCount() {
        return cabinetsCount;
    }

    public void setCabinetsCount(int cabinetsCount) {
        this.cabinetsCount = cabinetsCount;
    }

    public int getStoveCount() {
        return stoveCount;
    }

    public void setStoveCount(int stoveCount) {
        this.stoveCount = stoveCount;
    }

    public int getOvenCount() {
        return ovenCount;
    }

    public void setOvenCount(int ovenCount) {
        this.ovenCount = ovenCount;
    }

    public int getSinkCount() {
        return sinkCount;
    }

    public void setSinkCount(int sinkCount) {
        this.sinkCount = sinkCount;
    }


}
